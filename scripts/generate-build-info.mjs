#!/usr/bin/env node
/**
 * Pre-build script: extracts git info and writes src/generated/build-info.ts
 * so the frontend can display its own build metadata in the footer.
 */
import { execSync } from 'child_process'
import { writeFileSync, mkdirSync } from 'fs'
import { dirname, resolve } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

function getGitInfo() {
  try {
    const commit = execSync('git rev-parse --short HEAD', { encoding: 'utf-8' }).trim()
    const branch = execSync('git rev-parse --abbrev-ref HEAD', { encoding: 'utf-8' }).trim()
    const commitTime = execSync('git log -1 --format=%cI', { encoding: 'utf-8' }).trim()
    const buildTime = new Date().toISOString()
    return { commit, branch, commitTime, buildTime }
  } catch {
    // Graceful fallback when git is unavailable (e.g. docker build without .git)
    return {
      commit: 'unknown',
      branch: 'unknown',
      commitTime: 'unknown',
      buildTime: new Date().toISOString(),
    }
  }
}

const info = getGitInfo()
const content = `// Auto-generated by scripts/generate-build-info.mjs — do not edit manually.

export const FRONTEND_BUILD_INFO = {
  commit: ${JSON.stringify(info.commit)},
  branch: ${JSON.stringify(info.branch)},
  commitTime: ${JSON.stringify(info.commitTime)},
  buildTime: ${JSON.stringify(info.buildTime)},
} as const
`

const outDir = resolve(__dirname, '..', 'src', 'generated')
mkdirSync(outDir, { recursive: true })
writeFileSync(resolve(outDir, 'build-info.ts'), content, 'utf-8')
console.log('✓ Build info generated:', JSON.stringify(info))
